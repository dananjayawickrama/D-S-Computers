    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
         String Search = jTextField1.getText();
  
        if (Search.matches("^[0-9]+{9}+$")) {
            
              String sql = "SELECT No,Billno,Cname,Cid,Date,Total,Cash,Balance FROM customers_final_total_bills where Billno LIKE '%" + Search + "%'";

            try {
                pst = con.prepareStatement(sql);
                rs = pst.executeQuery();
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            } catch (SQLException ex) {

            }
          
        } else {
              
            JOptionPane.showMessageDialog(null, "Please enter Bill ID only!");
            jTextField1.setText(null); 
            billTotalTableLoad();
          
        }
    }//GEN-LAST:event_jButton1ActionPerformed

